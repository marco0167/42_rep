#include "ft_printf.h"

int	ft_print_string(char *str)
{
	int		i;
	char	*temp;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		temp = &str[i++];
		write(1, temp, 1);
	}
	return (i);
}
